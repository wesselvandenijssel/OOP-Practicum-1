public class whileNumberPrinter {
    public static void main(String[] args) {
        int i = 1; // Initialize the loop control variable

        // Using a while loop to iterate from 1 to 10
        while (i <= 10) {
            System.out.println(i); // Printing the current number
            i++; // Incrementing the loop control variable
        }
    }
}
